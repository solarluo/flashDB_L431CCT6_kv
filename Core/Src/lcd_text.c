static int font_index(char ch)
{
    if (ch >= 'a' && ch <= 'z')
    {
        ch = (char)(ch - 'a' + 'A');   // 小写先转大写显示
    }

    if (ch == ' ') return 0;
    if (ch == '!') return 1;
    if (ch == '"') return 2;
    if (ch == '#') return 3;
    if (ch == '$') return 4;
    if (ch == '%') return 5;
    if (ch == '&') return 6;
    if (ch == '\'') return 7;
    if (ch == '(') return 8;
    if (ch == ')') return 9;
    if (ch == '*') return 10;
    if (ch == '+') return 11;
    if (ch == ',') return 12;
    if (ch == '-') return 13;
    if (ch == '.') return 14;
    if (ch == '/') return 15;

    if (ch >= '0' && ch <= '9') return 16 + (ch - '0');
    if (ch == ':') return 26;
    if (ch >= 'A' && ch <= 'Z') return 27 + (ch - 'A');

    return 0;
}

static uint8_t lcd_char_supported(char ch)
{
    if (ch >= 'a' && ch <= 'z') return 1;
    if (ch == ' ' || ch == '!' || ch == '"' || ch == '#' || ch == '$' ||
        ch == '%' || ch == '&' || ch == '\'' || ch == '(' || ch == ')' ||
        ch == '*' || ch == '+' || ch == ',' || ch == '-' || ch == '.' ||
        ch == '/' || ch == ':' )
        return 1;

    if (ch >= '0' && ch <= '9') return 1;
    if (ch >= 'A' && ch <= 'Z') return 1;

    return 0;
}

void LCD_ClearArea(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t bgcolor)
{
    if (w == 0 || h == 0) return;
    ST7735_FillRect(x, y, w, h, bgcolor);
}

void LCD_ClearLine(uint16_t y, uint16_t bgcolor)
{
    if (y >= ST7735_HEIGHT) return;
    ST7735_FillRect(0, y, ST7735_WIDTH, LCD_FONT_HEIGHT, bgcolor);
}

void LCD_ShowCharEx(uint16_t x, uint16_t y, char ch, uint16_t color, uint16_t bgcolor, uint8_t transparent)
{
    if (x >= ST7735_WIDTH || y >= ST7735_HEIGHT)
        return;

    int idx = font_index(ch);

    for (uint16_t row = 0; row < LCD_FONT_HEIGHT; row++)
    {
        uint8_t line = font8x16[idx][row];

        for (uint16_t col = 0; col < LCD_FONT_WIDTH; col++)
        {
            if (line & (0x80 >> col))
            {
                ST7735_DrawPixel(x + col, y + row, color);
            }
            else
            {
                if (transparent == LCD_TEXT_OPAQUE)
                {
                    ST7735_DrawPixel(x + col, y + row, bgcolor);
                }
            }
        }
    }
}

void LCD_ShowChar(uint16_t x, uint16_t y, char ch, uint16_t color, uint16_t bgcolor)
{
    LCD_ShowCharEx(x, y, ch, color, bgcolor, LCD_TEXT_OPAQUE);
}

void LCD_ShowStringEx(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bgcolor, uint8_t transparent)
{
    if (str == NULL) return;

    uint16_t start_x = x;

    while (*str)
    {
        if (*str == '\n')
        {
            x = start_x;
            y += LCD_FONT_HEIGHT;
            str++;
            continue;
        }

        if (!lcd_char_supported(*str))
        {
            // 不支持的字符先显示为空格
            LCD_ShowCharEx(x, y, ' ', color, bgcolor, transparent);
        }
        else
        {
            LCD_ShowCharEx(x, y, *str, color, bgcolor, transparent);
        }

        x += LCD_FONT_WIDTH;

        if ((x + LCD_FONT_WIDTH) > ST7735_WIDTH)
        {
            x = start_x;
            y += LCD_FONT_HEIGHT;
        }

        if ((y + LCD_FONT_HEIGHT) > ST7735_HEIGHT)
        {
            return;
        }

        str++;
    }
}

void LCD_ShowString(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bgcolor)
{
    LCD_ShowStringEx(x, y, str, color, bgcolor, LCD_TEXT_OPAQUE);
}

void LCD_ShowStringInArea(uint16_t x, uint16_t y,
                          uint16_t area_w, uint16_t area_h,
                          const char *str,
                          uint16_t color, uint16_t bgcolor,
                          uint8_t transparent)
{
    if (str == NULL) return;
    if (area_w == 0 || area_h == 0) return;

    if (transparent == LCD_TEXT_OPAQUE)
    {
        LCD_ClearArea(x, y, area_w, area_h, bgcolor);
    }

    uint16_t cur_x = x;
    uint16_t cur_y = y;

    while (*str)
    {
        if (*str == '\n')
        {
            cur_x = x;
            cur_y += LCD_FONT_HEIGHT;
            str++;
            continue;
        }

        if ((cur_x + LCD_FONT_WIDTH) > (x + area_w))
        {
            cur_x = x;
            cur_y += LCD_FONT_HEIGHT;
        }

        if ((cur_y + LCD_FONT_HEIGHT) > (y + area_h))
        {
            return;
        }

        if (!lcd_char_supported(*str))
        {
            LCD_ShowCharEx(cur_x, cur_y, ' ', color, bgcolor, transparent);
        }
        else
        {
            LCD_ShowCharEx(cur_x, cur_y, *str, color, bgcolor, transparent);
        }

        cur_x += LCD_FONT_WIDTH;
        str++;
    }
}

void LCD_ShowInt(uint16_t x, uint16_t y, int value, uint16_t color, uint16_t bgcolor)
{
    char buf[16];
    snprintf(buf, sizeof(buf), "%d", value);
    LCD_ShowString(x, y, buf, color, bgcolor);
}

void LCD_ShowUInt(uint16_t x, uint16_t y, uint32_t value, uint16_t color, uint16_t bgcolor)
{
    char buf[16];
    snprintf(buf, sizeof(buf), "%lu", (unsigned long)value);
    LCD_ShowString(x, y, buf, color, bgcolor);
}

uint16_t LCD_GetStringWidth(const char *str)
{
    uint16_t width = 0;
    uint16_t line_width = 0;

    if (str == NULL) return 0;

    while (*str)
    {
        if (*str == '\n')
        {
            if (line_width > width)
                width = line_width;
            line_width = 0;
        }
        else
        {
            line_width += LCD_FONT_WIDTH;
        }
        str++;
    }

    if (line_width > width)
        width = line_width;

    return width;
}

void LCD_ShowCenteredString(uint16_t y, const char *str, uint16_t color, uint16_t bgcolor)
{
    uint16_t w = LCD_GetStringWidth(str);
    uint16_t x = 0;

    if (w < ST7735_WIDTH)
    {
        x = (ST7735_WIDTH - w) / 2;
    }

    LCD_ShowString(x, y, str, color, bgcolor);
}